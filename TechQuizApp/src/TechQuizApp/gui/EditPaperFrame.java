public void loadLanguage()
    {
        try
        {
            jcSubject.removeAllItems();
            ArrayList<String> language=ExamDAO.getLanguage();
            for(String l:language)
                jcSubject.addItem(l);
            
        }
        catch(SQLException ex)
        {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Exception in load language ","Exception",JOptionPane.ERROR_MESSAGE);
        }
    }

