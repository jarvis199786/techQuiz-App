    private void jcSubjectItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jcSubjectItemStateChanged
        // TODO add your handling code here:
          if(evt.getStateChange()==ItemEvent.DESELECTED)
            return;
        try
        {
            String language=jcSubject.getSelectedItem().toString();
            if(language.isEmpty())
            {
                JOptionPane.showMessageDialog(null, "No record found","no record",JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            ArrayList<String> examid=ExamDAO.getExamId(language);
            jcExamid.removeAllItems();
            for(String e:examid)
                jcExamid.addItem(e);
        }
        catch(SQLException ex)
        {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error while connecting to DB!", "Exception!", JOptionPane.ERROR_MESSAGE);

        }
    }//GEN-LAST:event_jcSubjectItemStateChanged

