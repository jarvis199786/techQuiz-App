    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        int response=validateInput();
        if(response==0)
        {
              JOptionPane.showMessageDialog(null,"UserId or Password Can Not be Left Blank !","Error !",JOptionPane.WARNING_MESSAGE);
              return;
        } 
        if(validateInput()==-1)
        {
              JOptionPane.showMessageDialog(null,"Password and RetypePassword must be Same !","Error !",JOptionPane.WARNING_MESSAGE);
              return;
        }      
        if(validateInput()==-2)
        {
              JOptionPane.showMessageDialog(null,"UserId and Password must be atleast 4 characters","Error !",JOptionPane.WARNING_MESSAGE);
              return;
        }    
         
             try
             {
                 UserPojo user=new UserPojo();
                 user.setUserid(userid);
                 user.setPassword(password);
                 user.setUsertype("student");
                  boolean status=UserDAO.addUser(user);
                  if(status)
                  {
                          JOptionPane.showMessageDialog(null,"Registration Successfully Done","UserId:"+userid+"\nPassword:"+password,JOptionPane.INFORMATION_MESSAGE);
                          clearAll();
                  }
                  else
                  {
                          JOptionPane.showMessageDialog(null,"User already Present","Try Login as a Student !",JOptionPane.WARNING_MESSAGE);

                  }
             }
            catch(SQLException ex)
           {
                   ex.printStackTrace();
                   JOptionPane.showMessageDialog(null,"Error while connecting to the DB !"+ex,"Exception",JOptionPane.ERROR_MESSAGE);
           }       
         
             
    }//GEN-LAST:event_btnRegisterActionPerformed

