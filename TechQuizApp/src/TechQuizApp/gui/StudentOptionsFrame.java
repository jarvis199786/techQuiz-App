    private void btnDoTaskActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDoTaskActionPerformed
        if(validateInputs()==false)
        {
            JOptionPane.showMessageDialog(null,"please make a selection first","Warning !",JOptionPane.WARNING_MESSAGE);
            return;
        }           
 
        else if(jrbTakeTest.isSelected())
        {
            ChoosePaperFrame choosePaperFrame=new ChoosePaperFrame();
            choosePaperFrame.setVisible(true);
            this.dispose();
        }
        else if(jrbViewScores.isSelected())
        {
            try   
            {
                ArrayList<String> examIdList=PerformanceDAO.getAllExamId(UserProfile.getUsername());
                if(examIdList.size()==0){
                JOptionPane.showMessageDialog(null,"You have not appeared for any exam yet!","No Records !",JOptionPane.INFORMATION_MESSAGE);
                return;
                }
                 ViewStudentScoreFrame viewStudentScoreFrame=new ViewStudentScoreFrame(examIdList);
                 viewStudentScoreFrame.setVisible(true);    
            }
           catch(SQLException ex)
           {
                   ex.printStackTrace();
                   JOptionPane.showMessageDialog(null,"Error while connecting to the DB !"+ex,"Exception",JOptionPane.ERROR_MESSAGE);
           }    
           this.dispose();
            
        }
        if(jrbChangePassword.isSelected())
        {
            ChangePasswordFrame changePasswordFrame=new ChangePasswordFrame();
            changePasswordFrame.setVisible(true);
            this.dispose();
        }
        

    }//GEN-LAST:event_btnDoTaskActionPerformed

