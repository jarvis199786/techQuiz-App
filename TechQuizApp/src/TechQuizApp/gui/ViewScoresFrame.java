    private void btnDoTaskActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDoTaskActionPerformed
        if(validateInputs()==false){
           JOptionPane.showMessageDialog(null,"please make a selection first","Warning !",JOptionPane.WARNING_MESSAGE);
           return;
        }
 
        if(jrbViewScore.isSelected())
        {
            ViewSingleScoreFrame viewScoresFrame=new ViewSingleScoreFrame();
            viewScoresFrame.setVisible(true);
            this.dispose();
        }
        else if(jrbViewAllScores.isSelected())
        {
            ViewAllStudentsScoreFrame viewAllFrame=new ViewAllStudentsScoreFrame();
            viewAllFrame.setVisible(true);
            this.dispose();
        }
     
    }//GEN-LAST:event_btnDoTaskActionPerformed

